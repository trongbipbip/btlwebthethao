const calendar = document.getElementById("calendar");
const searchBar = document.getElementById("searchBar");

function renderTournaments(filter = "") {
  calendar.innerHTML = "";

  Object.keys(tournamentsData).forEach((year) => {
    const yearDiv = document.createElement("div");
    yearDiv.classList.add("year");
    yearDiv.textContent = year;

    const eventsDiv = document.createElement("div");
    eventsDiv.classList.add("events");

    let hasMatch = false;

    tournamentsData[year].forEach((event) => {
      if (!filter || event.name.toLowerCase().includes(filter.toLowerCase())) {
        hasMatch = true; // Đánh dấu có giải đấu phù hợp

        const eventItem = document.createElement("div");
        eventItem.classList.add("event-item");

        const eventImg = document.createElement("img");
        eventImg.src = event.image;
        eventImg.alt = event.name;

        const eventInfo = document.createElement("div");
        eventInfo.classList.add("event-info");
        eventInfo.onclick = function () {
          window.location.href = `event.html?id=${event.id}`;
        };

        const eventName = document.createElement("p");
        eventName.textContent = event.name;

        const eventMore = document.createElement("span");
        eventMore.textContent = event.more;

        eventInfo.appendChild(eventName);
        eventInfo.appendChild(eventMore);

        eventItem.appendChild(eventImg);
        eventItem.appendChild(eventInfo);
        eventsDiv.appendChild(eventItem);
      }
    });

    if (hasMatch) {
      yearDiv.onclick = function () {
        const isOpen = eventsDiv.style.display === "block";
        eventsDiv.style.display = isOpen ? "none" : "block";
        this.classList.toggle("open", !isOpen);
      };

      calendar.appendChild(yearDiv);
      calendar.appendChild(eventsDiv);
      eventsDiv.style.display = "block"; // Mở năm có giải đấu phù hợp
      yearDiv.classList.add("open");
    }
  });
}

// Lắng nghe sự kiện nhập liệu trong ô tìm kiếm
searchBar.addEventListener("input", (e) => {
  renderTournaments(e.target.value);
});

// Hiển thị tất cả giải đấu khi trang load
renderTournaments();
